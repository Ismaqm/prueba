package main;
public class tablaGlobal {
	
	private int adecuFinal;
	private int mantenibilidad;
	private int valor;
	public tablaGlobal(int adecuFinal, int mantenibilidad, int valor) {
		super();
		this.adecuFinal = adecuFinal;
		this.mantenibilidad = mantenibilidad;
		this.valor = valor;
	}
	public int getAdecuFinal() {
		return adecuFinal;
	}
	public void setAdecuFinal(int adecuFinal) {
		this.adecuFinal = adecuFinal;
	}
	public int getMantenibilidad() {
		return mantenibilidad;
	}
	public void setMantenibilidad(int mantenibilidad) {
		this.mantenibilidad = mantenibilidad;
	}
	public int getValor() {
		return valor;
	}
	public void setValor(int valor) {
		this.valor = valor;
	}
	
	
	
	

}
